# Classifier tokens that carry no license identity and should be dropped when
# splitting multi-classifier strings (e.g. "DFSG approved; MIT License").
_CLASSIFIER_NOISE: frozenset[str] = frozenset(
    {
        "dfsg approved",
        "osi approved",
        "public domain",
    }
)

# Splits a compound SPDX expression into individual identifiers.
# Handles AND/OR operators and parentheses, e.g.:
#   "MPL-2.0 AND (Apache-2.0 OR MIT)" → ["MPL-2.0", "Apache-2.0", "MIT"]
_SPDX_SPLIT = re.compile(r"\bAND\b|\bOR\b|\bWITH\b|[()]")


def _normalize_spdx(license_str: str) -> str:
    """Return the canonical SPDX identifier for a pip-licenses license string.

    Handles three failure modes from pip-licenses output:
    - License text bleeding into the field (multi-line or comment-prefixed)
    - Multiple semicolon-joined PyPI classifier values
    - Simple strings not yet in the map (returned unchanged)
    """
    raw = license_str.strip()

    # Detect license text bleeding into the License field: pip-licenses
    # sometimes puts the raw file content here for packages without clean
    # metadata. Extract the first meaningful line and try to normalize that.
    if "\n" in raw or raw.startswith("#"):
        for line in raw.splitlines():
            line = line.strip().lstrip("#").strip()
            if line:
                raw = line
                break

    # Handle multiple classifiers joined with ";" (PyPI trove classifier style).
    # Map each recognisable part to SPDX and join with " AND " (conservative —
    # treat all listed licenses as simultaneously applicable).
    if ";" in raw:
        parts = [p.strip() for p in raw.split(";")]
        normalized = [
            _SPDX_MAP.get(p.lower(), p)
            for p in parts
            if p.lower() not in _CLASSIFIER_NOISE
        ]
        return " AND ".join(normalized) if normalized else raw

    return _SPDX_MAP.get(raw.lower(), raw)


def _extract_spdx_ids(expression: str) -> list[str]:
    """Extract individual SPDX identifiers from a compound expression.

    Strips AND/OR/WITH operators and parentheses, returning only the
    identifiers themselves (e.g. ["MPL-2.0", "Apache-2.0", "MIT"]).
    """
    tokens = [t.strip() for t in _SPDX_SPLIT.split(expression)]
    return [t for t in tokens if t]


def _license_label(full_path: str) -> str:
    """Strip the absolute venv prefix from a license path, keeping the path
    relative to the dist-info directory (e.g. 'licenses/LICENSE')."""
    parts = Path(full_path).parts
    for i, part in enumerate(parts):
        if part.endswith(".dist-info"):
            return str(Path(*parts[i + 1 :]))
    return Path(full_path).name


def main() -> None:
    if len(sys.argv) != 5:
        print(
            f"Usage: {sys.argv[0]} <input.json> <output.md> <output.csv> <licenses.toml>",
            file=sys.stderr,
        )
        sys.exit(1)

    input_path = Path(sys.argv[1])
    output_md = Path(sys.argv[2])
    output_csv = Path(sys.argv[3])
    config_path = Path(sys.argv[4])

    with config_path.open("rb") as f:
        config = tomllib.load(f)

    allow: set[str] = set(config["licenses"]["allow"])
    deny: set[str] = set(config["licenses"]["deny"])
    # Build exception lookup keyed by (name, version) — python-typed entries only
    exceptions: dict[tuple[str, str], dict] = {
        (e["name"], e["version"]): e
        for e in config.get("exceptions", [])
        if e.get("type") == "python"
    }

    data = json.loads(input_path.read_text())

    # Normalize License field to SPDX IDs
    for pkg in data:
        pkg["License"] = _normalize_spdx(pkg.get("License", "UNKNOWN"))

    # Apply per-package exceptions: override License and optionally LicenseText
    for pkg in data:
        key = (pkg["Name"], pkg["Version"])
        if key in exceptions:
            exc = exceptions[key]
            pkg["License"] = exc["spdx"]
            if "license_text" in exc:
                pkg["LicenseText"] = exc["license_text"]

    # Write normalized JSON back so it can be used for compliance tooling
    input_path.write_text(json.dumps(data, indent=2))

    # Validate all packages against allow/deny lists
    failures: list[str] = []
    exception_keys = set(exceptions.keys())
    for pkg in data:
        key = (pkg["Name"], pkg["Version"])
        if key in exception_keys:
            continue
        license_str = pkg.get("License", "UNKNOWN")
        ids = _extract_spdx_ids(license_str)
        for spdx_id in ids:
            if spdx_id in deny:
                failures.append(
                    f"  DENIED   {pkg['Name']} ({pkg['Version']}): {spdx_id}"
                )
            elif spdx_id not in allow:
                failures.append(
                    f"  UNKNOWN  {pkg['Name']} ({pkg['Version']}): {spdx_id!r} not in allow list"
                )

    if failures:
        print("License validation failed:\n" + "\n".join(failures), file=sys.stderr)
        sys.exit(1)

    # Filter first-party: we don't attribute ourselves. Matches pyproject.toml
    # project.name. Update if the project is ever renamed.
    sorted_data = sorted(
        (pkg for pkg in data if pkg["Name"].lower() != "aiperf"),
        key=lambda x: x["Name"].lower(),
    )

    with output_md.open("w") as f:
        f.write(_HEADER)
        for pkg in sorted_data:
            f.write(f"\n## {pkg['Name']} ({pkg['Version']})\n")
            f.write("\n### Licenses\n")
            f.write(f"License: `{pkg.get('License', 'UNKNOWN')}`\n\n")
            text = pkg.get("LicenseText", "").strip()
            if text:
                label = _license_label(pkg.get("LicensePath", "")) or "LICENSE"
                f.write(f"  - `{label}`:\n```\n{text}\n```\n\n")
            url = pkg.get("URL", "")
            if url and url not in ("", "UNKNOWN"):
                f.write("### URLs\n")
                f.write(f"  - `Homepage`: {url}\n")
            f.write("\n")

    with output_csv.open("w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["dependency_type", "name", "version", "spdx_license"])
        for pkg in sorted_data:
            writer.writerow(
                ["python", pkg["Name"], pkg["Version"], pkg.get("License", "UNKNOWN")]
            )


if __name__ == "__main__":
    main()
