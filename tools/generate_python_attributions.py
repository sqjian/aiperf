"""Generate attribution files from pip-licenses JSON output.

Normalizes license identifiers to SPDX IDs, validates them against an
allow/deny list, then writes an updated JSON (with SPDX IDs), a human-readable
markdown file, and a CSV summary for compliance tooling.

Usage:
    python3 tools/generate_python_attributions.py \\
        <input.json> <output.md> <output.csv> <licenses.toml>
"""

